#include "math.h"
/**
*add - add 2 nums
*@a: num1
*@b: num2
*Return: sum
*/
int add(int a, int b)
{
	return (a + b);
}
/**
*sub - sub 2 nums
*@a: num1
*@b: num2
*Return: difference
*/
int sub(int a, int b)
{
	return (a - b);
}
/**
*mul - mul 2 nums
*@a: num1
*@b: num2
*Return: product
*/
int mul(int a, int b)
{
	return (a * b);
}
